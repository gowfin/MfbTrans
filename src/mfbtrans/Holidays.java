    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
String dd=jDateChooser1.getDate().toString();
        String m="";
        if(dd.contains("Jan")){m="01";}
        else if(dd.contains("Feb")){m="02";}
         else if(dd.contains("Mar")){m="03";}
         else if(dd.contains("Apr")){m="04";}
         else if(dd.contains("May")){m="05";}
         else if(dd.contains("Jun")){m="06";}
        else if(dd.contains("Jul")){m="07";}
        else if(dd.contains("Aug")){m="08";}
        else if(dd.contains("Sep")){m="09";}
        else if(dd.contains("Oct")){m="10";}
        else if(dd.contains("Nov")){m="11";}
        else if(dd.contains("Dec")){m="12";}
String SelectedDate= dd.substring(dd.length()-4)+"-"+m+"-"+dd.substring(8,10);
SelectedDate=SelectedDate+" 00:00:00.000";
String loans[]= new String[10000];
this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
 try
            {
           
             PreparedStatement pstmt;
                      String sql="select loanid from Loanschedule " +
" where date='"+SelectedDate+"'";
       
Connection con = glob.getConnect();
             
            Statement stmt = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_READ_ONLY);
            ResultSet current = stmt.executeQuery(sql);
            int i=0;
           while( current.next()){
             loans[i]=current.getString("loanid");
             i=i+1;
           }
          // JOptionPane.showMessageDialog(rootPane, loans);
          for(int j=0;j<i;j++){
           current = stmt.executeQuery("select LoanProduct,MAX(ls.count) tenor from loans   l inner join Loanschedule ls " +
" on l.LoanID=ls.LoanID where l.LoanID='"+loans[j]+"'" +
" group by LoanProduct"); 

           if(current.next()){
            
                    if(current.getString("loanproduct").contains("REGLN") && current.getInt("tenor")<15 ) {
                  pstmt=con.prepareStatement("update loanschedule set date=date+14 where date>='"+SelectedDate+"' and loanid='"+loans[j]+"'");
                  pstmt.executeUpdate();
                                        } 
                    else if(current.getString("loanproduct").contains("REGLN") && current.getInt("tenor")>23 ) {
               pstmt=con.prepareStatement("update loanschedule set date=date+7 where date>='"+SelectedDate+"' and loanid='"+loans[j]+"'");
                  pstmt.executeUpdate();
                        }
                    else{
                     pstmt=con.prepareStatement("update loanschedule set date=date+1 where date='"+SelectedDate+"' and loanid='"+loans[j]+"'");
                  pstmt.executeUpdate();    
                    }
           }
              
              
          }
JOptionPane.showMessageDialog(rootPane, "Loans Repayment successfully rescheduled");
            }
            catch(Exception e){JOptionPane.showMessageDialog(rootPane, e.getMessage());}
 this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));

    }//GEN-LAST:event_jButton1ActionPerformed

