    private void jButtonMonthlyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMonthlyActionPerformed
        Calendar now=Calendar.getInstance();
        now.getTime();
        int year=now.get(Calendar.YEAR);
        String Smonth,Sday;
        int Omonth=now.get(Calendar.MONTH);//opening as january=0 not 1
        int day=now.get(Calendar.DAY_OF_MONTH);
        int month=Omonth+1;//note month start with january=0 in java
        Smonth=month+"";
        Sday=day+"";
        if(month<10){Smonth="0"+month;}// let it appear as two digit e.g 02
        if(day<10){Sday="0"+day;}
        String startdate=year+"-"+Smonth+"-01";

        String enddate=year+"-"+Smonth+"-"+Sday;
        // JOptionPane.showMessageDialog(this, startdate+" and "+enddate);
        String query="select distinct(t.accountid), d.runningbal, d.productid,lasttrans,d.accountid from deposit d inner join transactn t" +
        " on d.accountid=t.accountid" +
        " where d.productid='Daily Savings' and amount>0 and status='Active' and t.dateeffective between '"+startdate+"' and '"+enddate+"'";

        Global glob = new Global();
        Connection    conn = glob.getConnect();
        PreparedStatement pstmt;
        try{
            Statement stmt = conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_READ_ONLY);

            ResultSet current = stmt.executeQuery(query);
            //to eliminate empty rows

            String BOD[][]= new String[500][3];

            int i=0;

            while(current.next()){

                BOD[i][0]= current.getString("accountid");
                BOD[i][1]=current.getString("lasttrans");
                BOD[i][2]=current.getString("RunningBal");
                i=i+1;

            }
            //JOptionPane.showMessageDialog(this,BOD);
            int ii=i;
            for(int r=0;r<ii;r++){

                glob.withdrawDeposit(Double.parseDouble(BOD[r][1]),BOD[r][2],BOD[r][0],"3.333%Removed");
                String q="update deposit set status='M' where accountid='"+ BOD[r][0]+"'";
                pstmt=conn.prepareStatement(q);//
                pstmt.executeUpdate();

            }
            UserLogin ulogin = new UserLogin();
            ulogin.jButtonautopay.doClick();
            JOptionPane.showMessageDialog(this,"Month-End Process Successful");
        }

        catch(Exception e){JOptionPane.showMessageDialog(this, e.getMessage());}
    }//GEN-LAST:event_jButtonMonthlyActionPerformed

